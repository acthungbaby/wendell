		$(document).ready(function() {
			$('#menu-productos ul .first').addClass('activo');
			$("#submenu-productos ul").addClass('visible')
			$('#submenu-productos ul .first').addClass('activo');

			$(".opcionuno").removeClass('oculto');

			$("#menu-productos ul li, #submenu-productos ul li").click(function(){
				$('.activo').removeClass('activo');
				$(this).addClass("activo");
				var attr = $(this).attr("link");


				var article = $(this).attr("article");

				$('article:not([oculto])').addClass('oculto')

				$("."+article).removeClass('oculto');

				if (article=='opcionuno') {
					$('#submenu-productos ul .first').addClass('activo');
				};

				if (article=='opcionseis') {
					$('#contenedorMain .banner').removeClass('background-productos');
					$('#contenedorMain .banner').addClass('background-melaza');
				}else{
					if ($('#contenedorMain .banner').hasClass('background-melaza')){
						$('#contenedorMain .banner').removeClass('background-melaza');
						$('#contenedorMain .banner').addClass('background-productos');
					}
				};

				if (attr=="submenu") {
					$("#submenu-productos ul").addClass('visible')
					$('#menu-productos ul .first').addClass('activo');
					//$('#submenu-productos ul .first').addClass('activo');
					
				}else{
					$("#submenu-productos ul").removeClass('visible')
				};



			});
		});
