				<h3>Medio Ambiente</h3>
				<p>En Wendell Trading Inc. estamos comprometidos a cuidar el medio ambiente para que futuras generaciones de paraguayos puedan disfrutar de lo que consideramos es una de nuestras mayores riquezas. Es por este motivo que desarrollamos nuestra producción bajo estrictas normas orgánicas, es decir, sin uso de químicos, ni en las plantaciones, ni en el proceso de producción.</p>
