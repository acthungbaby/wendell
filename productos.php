						$('#contenedorMain .banner').removeClass('background-melaza');
						$('#contenedorMain .banner').addClass('background-productos');
					}
				};

				if (attr=="submenu") {
					$("#submenu-productos ul").addClass('visible')
					$('#menu-productos ul .first').addClass('activo');
