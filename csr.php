				<h3>Environment</h3>
				<p>In Wendell Trading Inc., we are committed to caring for the environment to ensure that future generations of Paraguayans may enjoy what we consider to be our greatest source of wealth. Thus, we carry out our activities following strict norms for organic production—that is, avoiding the use of chemicals—in our fields and our production process.</p>
				<p class="margin">Throughout the years, different certifiers have supported our efforts to care for the environment. From 1996 to 1999, the company was certified by Oregon Tilth for its commitment to the environment, and from 2000 until now, we have obtained certification from IMO Control. Wendell Trading Inc. also has an environmental license issued by the Ministry of the Environment, which certifies our good environmental practices.</p>
				<h3 class="margin">The Community</h3>
				<p>Social responsibility is an important pillar for us, and we try to reflect this in each and every one of our actions. Our company has a long tradition of social responsibility with the community in different areas such as education, sports and trade. We seek to establish a friendly work environment in which values are respected and people can develop and grow professionally. Early on, the company identified the need to help and support its community, and it is with this vision that in the 1930’s, it donated the land where the public school Nº 95 “Dr. Manuel Peña” was built. Later on, in the 1950’s, the company donated another piece of land for the construction of a community center in which the sugar cane producers could gather. This led to the creation of the Benjamin Aceval Sugar Cane Producers’ Association. In the 80’s, in an effort to contribute to the community’s continuous growth, the company donated the land in which the Sugar Sports Club was later built with funds raised by selling other land donated by Wendell Trading Inc.</p>
				<p class="margin">Presently, we continue to support the community and its institutions. We are aware of the important role health and education has in the growth and development of our children, which is why we contribute by donating sugar to the public schools of Benjamin Aceval.</p>
				<p class="margin">We support sports by providing sports clothes for the soccer players of the Sugar Club and the 15 de Agosto Club in every one of their championships.</p>
				<p class="margin">We also fight for the inclusion of indigenous communities in our activities, in an attempt to improve their quality of life while respecting their culture and lifestyle.</p>
				<h3 class="margin">Sustainability for Producers</h3>
				<p>Our activities give approximately 500 small producers the opportunity to sell their sugar with their own brand and export it with the Fair Trade certification, therefore improving their quality of life as well as that of their families and at the same time promote their community’s sustainable development.</p>
