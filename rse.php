				<h3 class="margin">La Comunidad</h3>
				<p>La responsabilidad social es un pilar importante para nosotros, y tratamos de que esta sea reflejada en todas nuestras acciones. Nuestra empresa tiene una larga tradición de responsabilidad social con la comunidad en diferentes ámbitos como ser educación, deporte y en lo gremial. Buscamos crear un ambiente amigable con nuestro entorno de trabajo, donde primen los valores y las personas puedan desarrollarse y crecer profesionalmente. Desde temprano, vimos la necesidad de ayudar y apoyar a nuestra comunidad, y fue por esta visión, que en la década de los 30, donamos el terreno donde se edificó la escuela Básica Nº 95 “Dr. Manuel Peña”. Más tarde, en los 50, donamos un terreno y llevamos a cabo la construcción del local donde productores de caña de azúcar de la zona pudieran nuclearse y formar así la Asociación de Productores de Caña de Azúcar de Benjamín Aceval. En la década de los 80, buscando aportar al crecimiento continuo de la comunidad, donamos el predio donde más adelante se construiría el Club del Sportivo Azucarero, con fondos recaudados de la venta de otros terrenos también donados por la empresa.</p>
